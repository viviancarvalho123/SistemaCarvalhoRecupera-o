    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
         generosVc = viewBean();

        if (incluindo == true) {
            generosVc_DAO.insert(generosVc);
        } else {
            generosVc_DAO.update(generosVc);
        }

        Util.habilitar(true,jTxtCódigoGenero, jTxtNome, jTxtDescricao, jFmtAnoOrigem, jBtnCancelar, jBtnConfirmar);
        Util.habilitar(false, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);

        Util.limparCampos(jTxtCódigoGenero, jTxtNome, jTxtDescricao,jFmtAnoOrigem);
        
         Util.habilitar(false, jTxtCódigoGenero, jTxtNome, jTxtDescricao,jFmtAnoOrigem,  jBtnCancelar, jBtnConfirmar);
        Util.habilitar(true, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);
        
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

