     public GenerosVc viewBean() {
        GenerosVc generosVc = new GenerosVc();
        int id = Integer.valueOf(jTxtCódigoGenero.getText());
        generosVc.setIdgenerosVc(id); 
        generosVc.setNomeVc(jTxtNome.getText());
        generosVc.setDescricaoVc(jTxtDescricao.getText());
        generosVc.setAnoOrigemVc(jFmtAnoOrigem.getText());    
        generosVc.setNotasVc(jTxtNotas.getText());
       
      
    return generosVc;
    }
    
     
      public void beanView(GenerosVc generosVc) {
        String cad = String.valueOf(generosVc.getIdgenerosVc());
        jTxtCódigoGenero.setText(cad);   
        jTxtNome.setText(generosVc.getNomeVc());
        jTxtDescricao.setText(generosVc.getDescricaoVc());
        SimpleDateFormat formato = new SimpleDateFormat("MM/yyyy");
        jFmtAnoOrigem.setText( formato.format(generosVc.getAnoOrigemVc()));
        
       
    }

